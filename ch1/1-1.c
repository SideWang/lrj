#include <stdio.h>
/***********************************************************************************
功能：通过亦或进行两个变量的交换
作者：Side
时间：2017/11/16
***********************************************************************************/
int main(int argc, char const *argv[])
{
	int a,b;
	scanf("%d%d",&a,&b);
	a^=b;
	b^=a;
	a^=b;
	printf("%d%d\n",a,b);

	return 0;
}